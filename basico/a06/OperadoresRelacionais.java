package basico.a06;

public class OperadoresRelacionais {

    public static void main(String[] args) {
        
        int valor1 = 1;
        int valor2 = 2;

        System.out.println("Valor1 é == a Valor2? " + (valor1 == valor2));
        System.out.println("Valor1 é != a Valor2? " + (valor1 != valor2));
        System.out.println("Valor1 é > a Valor2? " + (valor1 > valor2));
        System.out.println("Valor1 é >= a Valor2? " + (valor1 >= valor2));
        System.out.println("Valor1 é < a Valor2? " + (valor1 < valor2));
        System.out.println("Valor1 é <= a Valor2? " + (valor1 <= valor2));

    }
    
}
